package item44;

/**
* This class is the representative of how to comment
* any Java code properly.
*/
public class Commented {
	/**
	* Returns the value of boxed integer
	* 
	* @param val Boxed integer value to be converted; should not be 
	* null
	* @return the integer value of boxed variable
	* @throws NullPointerException if {@code val==null}
	*/
	public static int valueOf(Integer val) {
		return Integer.valueOf(val);
	}
}
